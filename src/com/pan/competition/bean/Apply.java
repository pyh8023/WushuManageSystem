package com.pan.competition.bean;

public class Apply {
	private String id;
	private String athlets;
	private String event_id;
	private String apply_name;
	private String ranking;
	private String remark;
	private String delegation_id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAthlets() {
		return athlets;
	}
	public void setAthlets(String athlets) {
		this.athlets = athlets;
	}
	public String getEvent_id() {
		return event_id;
	}
	public void setEvent_id(String event_id) {
		this.event_id = event_id;
	}
	public String getApply_name() {
		return apply_name;
	}
	public void setApply_name(String apply_name) {
		this.apply_name = apply_name;
	}
	public String getRanking() {
		return ranking;
	}
	public void setRanking(String ranking) {
		this.ranking = ranking;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getDelegation_id() {
		return delegation_id;
	}
	public void setDelegation_id(String delegation_id) {
		this.delegation_id = delegation_id;
	}
}
